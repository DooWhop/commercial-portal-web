if (typeof allinpay !== 'object') var allinpay = {};
allinpay.chooseCityBySelect = function(el) {


	this.init = function(){
		$(el).citySelect({
			url:"plugins/city/city.min.js",
			required : false
		}); 
	}

}


// --------------------------------------------------------------------------------------------------------------------------------
// --------------------------------------------------------------------------------------------------------------------------------
// END


$(function(){
	(new allinpay.chooseCityBySelect("#nowCity")).init();
	(new allinpay.chooseCityBySelect("#idCardCity")).init();
});